#include "push_swap.h"

void	ft_isallnum(char* array)
{
	int i;

	i = 0;
	while (array[i] != '\0')
	{
		if ((array[i] >= 48 && array[i] <= 57) || (array[i] == ' ') ||
			((array[i] == '-' || array[i] == '+') && (array[i + 1] >= 48 && array[i + 1] <= 57)))
			i++;
		else
			ft_exit();
	}
}